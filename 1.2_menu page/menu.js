function plusButton1(){

    var i=0;
    i=document.getElementById("qtyInput01").value;
    i=Number(i);
    i++;
    document.getElementById("qtyInput01").value = i;
    
}
    
function minusButton1(){

    var i=0;
    i=document.getElementById("qtyInput01").value;
    i=Number(i);
    i--;
    if (i<0){
        i=0;
    }
    document.getElementById("qtyInput01").value = i;
    
}

